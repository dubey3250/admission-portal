MBA - Master of Business Administration
Duration : 2 years
Eligibility : As per PTU norms

MBA@RIMT is aimed at nurturing the youth for managerial & responsible positions in industry, trade, commerce and other domains of expertise. The program focuses on : Understanding the management & operations of organizations, Developing professional skills in business and management, Enhancing creative and analytical skills to improve business and management practices. 

RIMT offers dual specialization as well as super specialization in MBA: MARKETING, Consumer Behaviour, Advertising & Sales Management, FINANCE, Security Analysis & Portfolio Management, Management of Financial Services, OPERATIONS, Production Planning & Control, Quality Management, INFORMATION TECHNOLOGY, Programming in C/ C++, E-Commerce & IT enabled services, HUMAN RESOURCE MANAGEMENT, Social Security & Labour Welfare, Industrial Relation & Labour Laws

We at RIMT, achieve all this through well managed curricular and extra-curricular activities, delivered by experts from academia and industry. Special stress is laid on developing communication skills, problem-solving abilities, handling responsibility, good work habits, team work, developing positive attitude.

