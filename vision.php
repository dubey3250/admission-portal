<span style="margin-left:10px;font-size:40px;color:indigo;">Vision</span><br/>
<span style="margin-left:5px;font-size:25px;">
The bill for the establishment of VIT University was placed in the Rajya Sabha on September 1, 1965 by the then Education Minister, Mr M.C. Chagla. During the discussion that followed, Hon. M.P. Mr. Bhushan Gupta voiced the opinion that this should not be yet another university. New faculties should be created, including scientific socialism, and one thing that this university should ensure was to keep noble ideas in mind and provide accessibility to students from weaker sections of society. The VIT Bill was passed in Lok Sabha on 16th November 1966 and the VIT Act came into force on 22nd April, 1969.

The University is located, since 1976, on a campus that is spread over approximately 1000 acres of land on the Vellore range in Chennai, which has now been turned into a green and open area though many academic buildings, hostels, and residences for faculty and staff have come up during the last forty years. Presently the University has over 7300 students, who come from all parts of the country and abroad, and from all sections of society. There are over 500 faculty members in addition to administrative staff who, along with students, live on campus, and they interact and mingle with each other irrespective of social or professional status.

The living ambience and social milieu of the campus is also reflected in an integrated, interdisciplinary approach in teaching and research. There is freedom to define and design course content or start new courses. Research themes evolve with new developments in the area and the interface between different areas of study. Everyone at the university competes with himself/herself to excel in their own field of research. VIT is academically and socially a vibrant place where all have space to express their views.

The ten Schools and four special centres of the university produce high quality research publications, books, working papers and M.Phil and Ph,D theses. The VIT alumni occupy important positions, in academics, government, private sector, and in fact in all walks of life. Recently VIT has been ranked by the NAAC as the top University in the country.

VIT will continue to play a major role in the field of higher education, by seeding new ideas and creating knowledge, and by imparting high level of training with values and social commitment.
