MCA - Master of Computer Applications
Duration : 3 years
Eligibility : As per PTU norms

MCA@RIMT is designed to meet the skill requirements of any IT or Application related organization. The main goal is to groom the students for productive career in software industry & academics. 

The curriculum includes internship in reputed organizations where students are encouraged to spend a full semester working in industry giving them insight in to the corporate world. The curriculum also has a strong core covering Operating system, Data Communication, RDBMS, OOP, Programming in C, Data structure, Java, Software Engg., Artificial Intelligence, Computer based Optimization methods, Accounting & Finance Management. Our MCA pass out students are currently working in well reputed companies. 

