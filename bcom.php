Bachelor of Commerce
From Wikipedia, the free encyclopedia
Jump to: navigation, search 
A Bachelor of Commerce (abbreviated BCom, B.Com., BCommerce, or B.Comm.) is an undergraduate degree in commerce and related subjects. The degree is also known as the Bachelor of Commerce and Administration, or BCA. It is predominantly offered in Commonwealth nations; however, the degree is no longer offered in the United Kingdom.[citation needed] It is generally offered as a full-time, three- or four-year program.

